                        <Text>{`\t\t\t\t北京、天津、南京、上海、武汉、广州、长沙的一些进步青年，在1919年底曾经兴起工读互助主义的实验活动。他们遵循工读互助主义的学说，按照“人人做工，人人读书，各尽所能，各取所需”的理想，组织互助社一类小团体，过起“共产的生活”，并希望把工读互助团逐渐推广到全社会，从而实现“平和的经济革命”。然而，这种工读互助团的试验好景不长。经过一段时间的实践，这类组织都因遇到种种无法克服的困难而解体。
\t\t\t\t与实践“工读互助”相类似，一些青年知识分子模仿日本九州的新村、美国的劳动共产村的做法，也在中国进行“新村”试验。同工读互助团的命运一样，这些中国的“新村”也是昙花一现。
\t\t\t\t“工读互助”和“新村”试验的失败，对热衷于空想社会主义和改良主义的人们，是一次非常实际的教育。许多参加“工读互助”和“新村”活动的青年开始认识到：“社会没有根本改造以前，不能试验新生活”；“要改造社会，须从根本上谋全体的改造，枝枝节节地一部分的改造是不中用的”①。他们很快抛弃不切实际的空想，转而寻找改造社会的“实际可循”的途径。这就为一批进步青年认识空想社会主义和其他类似的改良主义思想的谬误，最终选择科学社会主义作为自己的信仰，创造了有利的条件。
`}</Text>
