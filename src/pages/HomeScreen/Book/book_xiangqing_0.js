                        <Text>{`\t\t\t\t最先倡导并吹响思想启蒙号角的，是后来被誉为进步思想界的明星、“五四运动的总司令”的陈独秀。1915年9月，陈独秀在上海创办《青年杂志》，在思想文化领域掀起一场以民主和科学为旗帜，向传统的封建思想、道德、文化宣战的新文化运动。一年后，《青年杂志》更名为《新青年》。添加一个“新”字，不仅使其鼓吹新思想、新文化，启发新觉悟，造就新青年的主旨一目了然，而且给人以全新的感觉：起点新，内容新，目标新，形式新。该刊发表的李大钊的《青春》一文，不仅强调青年之青，而且强调一个“新”字，指出从精神上、思想上有新青年与旧青年之分，希望青年们站在时代前列，做一个有为的新青年。
\t\t\t\t《新青年》的主要撰稿人有陈独秀、李大钊、鲁迅、胡适、钱玄同、刘半农、高一涵、周作人、易白沙、吴虞等。《新青年》创刊时曾表示其宗旨不在“批评时政”，但这并不表明它不关心政治，实际上其作者明确认识到，他们在思想文化领域内所进行的斗争，是和政治密切相关的。他们反对IH思想、旧文化，实际上就是对旧政治的声讨。出于对辛亥革命失败的反思，他们不愿就事论事地议论现时的政治问题，而力图通过思想的启蒙促进政治的根本改革。
\t\t\t\t以《新青年》的出版为标志兴起的新文化运动，使20世纪初的中国，开始经历一场深刻的思想革命。在这场激烈的思想文化斗争中，陈独秀表现得十分勇敢和坚决，成为新文化运动的主将。
`}</Text>
