                        <Text>{`\t\t\t\t新文化运动倡导的民主，有两层涵义。一是指民主精神和民主思想。这包括个性解放、人格独立及自由民主权利等内容。二是指与封建君主专制制度相对立的资产阶级民主政治制度。新文化运动的思想家们尖锐地指出：“民与君不两立，自由与专制不并存”。中国要在世界上生存，“必弃数千年相传之官僚的、专制的个人政治，而易以自由的、自治的国民政治”。
\t\t\t\t新文化运动倡导的科学，也有两个方面的涵义。它主要是指与封建迷信、蒙昧无知相对立的科学思想、科学精神以及认识和判断事物的科学方法，同时也指具体的科学技术、科学知识。新文化运动的倡导者主张科学与人权并重，认为：“近代欧洲之所以优越他族者，科学之兴，其功不在人权说下，若舟车之有两轮焉。”他们强调要用科学的尺度衡量世间一切事物，崇尚理性，反对迷信和愚昧；指出只有“一遵理性”，具有科学精神，国人才能摆脱“无常识之思维，无理由之信仰”的束缚，成为新鲜活泼的一代新人。
\t\t\t\t新文化运动在民主和科学两面大旗的指引下，向封建主义思想文化发起前所未有的猛烈攻击，号召人们“冲决过去历史之网罗，破坏陈腐学说之囹圄”，在众多的领域掀起了思想解放的浪潮。
\t\t\t\t新文化运动把攻击的矛头集中指向统治中国两千多年的以纲常名教为核心的封建主义思想文化，特别是被袁世凯所尊奉的孔教。先进的思想家们以进化论观点和个性解放思想为主要武器，揭露三纲五常是“奴隶的道德”，忠孝节义是“吃人的礼教”，坚定地表示：“对于与此新社会、新国家、新信仰不可相容之孔教，不可不有彻底之觉悟，猛勇之决心，否则不塞不流，不止不行。”①这些“离经叛道”的呐喊，把反对封建的政治制度和反对封建的伦理道德结合起来，形成了一场空前的反封建运动。他们深刻地指出，封建伦理与共和制度是不相容的，拥护共和国体就必须反对封建伦理，从而透彻地阐述了新制度与旧文化绝对不可调和的道理。他们把这个认识称作“吾人最后之觉悟”。
`}</Text>
