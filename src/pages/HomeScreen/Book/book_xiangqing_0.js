                        <Text>{`\t\t\t\t20世纪初中国社会阶级关系的变化和新的革命力量的成长，为新的人民革命的到来创造了客观的社会基础。特别是无产阶级队伍的壮大，为中国共产党的创建奠定了阶级基础。与此同时，第一次世界大战对西方资本主义制度和文明的冲击，俄国十月社会主义革命的胜利和欧亚革命浪潮的高涨，则为新的人民革命的兴起提供了必要的时代条件和国际环境。在这种情况下，中国先进分子接受和传播马克思主义并建立中国共产党，就具有了现实的可能性。而将这种可能性转变为必然性，则是在五四运动的推动下实现的。`}</Text>
