                        <Text>{`\t\t\t\t在马克思主义传播过程中，还有一条特殊的渠道和一支特别的队伍——五四运动前后出国勤工俭学的青年知识分子。从1919年初到1920年底，全国各地赴法国勤工俭学的青年就有1600多人，还有一些人去了英国、德国和比利时。这些有志于改造中国的进步青年，直接接触到产生马克思主义的欧洲社会，在那里认真学习和探求真理。他们到工厂做工，亲身体验工人阶级的生活，思想感情逐渐发生根本性的变化。许多人经过反复的比较和推求，最后作出自己的抉择，走上无产阶级革命的道路，转变为马克思主义者。他们中的一些人还通过通信等方式，向国内思想界传播马克思主义。同时，一些原来由学生和华工组成的以“工读”、“互助”等为信条的勤工俭学组织，也转向信仰共产主义。其中在勤工俭学励进会的基础上成立的工学世界社，于1920年12月底召开大会，表示要以马克思主义和实行俄国式的社会革命为宗旨。
\t\t\t\t五四运动以后，中国先进分子比较详细地介绍了马克思主义各组成部分的主要观点。在唯物史观方面，他们介绍了社会的发展根源于生产力与生产关系、经济基础与上层建筑相互矛盾运动等观点。在阶级斗争学说方面，着重阐释了阶级和阶级斗争的定义，阶级的划分和阶级之间的斗争是基于经济利益的不同的观点，以及国家是阶级斗争的工具，无产阶级必须掌握政权，建立多数人对少数人的专政等基本思想。在剩余价值理论方面，着重围绕资本的本质，介绍了剩余价值是资本家通过生产过程无偿占有工人劳动的一部分，是对无产阶级的剥削，是资本积累的重要组成部分等观点。此外，中国先进分子对马克思、恩格斯关于未来社会的描述也进行了介绍。
\t\t\t\t中国先进分子通过对马克思主义各个组成部分的观点及其相互之间联系的介绍，使人们对这一科学理论有了一个比较完整的认识。同时，他们又认真地把马克思主义和其他社会主义思潮进行比较，使人们认识到它们之间的联系与区别。他们的介绍和传播，引起了中国思想文化界特别是进步知识分子对马克思主义学说的极大兴趣。
`}</Text>
