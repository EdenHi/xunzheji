                        <Text>{`
\t\t\t\t第一次世界大战及其所引起的一系列灾难性后果，唤起了各国人民群众的觉醒，推动了各国革命运动的迅速高涨。
\t\t\t\t在俄国，工人和士兵于1917年3月(俄历2月)发动武装起义，推翻统治俄国300年之久的罗曼诺夫王朝，但国家政权最终落入由地主和资产阶级的代表人物组成的临时政府手中。1917年11月7日(俄历10月25日)，在列宁的领导下，彼得堡的工人群众发动武装起义，推翻反动的资产阶级临时政府。第二天，全俄工农兵苏维埃第二次代表大会通过列宁起草的《和平法令》和《土地法令》，以及其他一系列维护工农利益决议案。随后，苏维埃政权在俄国各地相继建立。俄国十月社会主义革命取得伟大胜利。
\t\t\t\t俄国十月革命的胜利，是人类历史上一个划时代的事件，极大地改变了20世纪世界历史的进程。它唤醒了西方的无产阶级，也唤醒了东方的被压迫民族。在十月革命的影响下，西方无产阶级争取社会主义的斗争与东方殖民地、半殖民地人民争取民族解放的斗争开始汇合，如海潮汹涌，冲击着资本主义世界的堤防。从此，世界上出现社会主义制度和资本主义制度相斗争、社会主义国家和资本主义国家相对立的新格局。
\t\t\t\t俄国十月革命胜利后，欧洲掀起一场革命风暴。从1917年11月开始，芬兰、德国、奥地利、匈牙利、捷克斯洛伐克、保加利亚等国相继爆发大规模的工人罢工运动和武装斗争，建立了一批地方性的工人苏维埃政权。在罗马尼亚、南斯拉夫、波兰，也爆发了大规模的革命运动。英国、法国和意大利等国家的工人运动迅速高涨。`}</Text>
