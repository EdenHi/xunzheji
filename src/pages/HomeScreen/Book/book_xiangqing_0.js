                        <Text>{`\t\t\t\t应该说，新文化运动的思想家们对于孔教的这些批判，不仅是极其勇敢的，也是充满理性的。诚如李大钊所指出的：“余之掊击孔子，非掊击孔子之本身，乃掊击孔子为历代君主所雕塑之偶像的权威也；非掊击孔子，乃掊击专制政治之灵魂也。”②
\t\t\t\t初期新文化运动的另一项主要内容是文学革命。1917年1月，胡适在《新青年》上发表《文学改良刍议》一文，较系统地提出文学改良的主张，鼓吹以白话文代替文言文，以白话文学为中国文学之正宗。陈独秀随即写了《文学革命论》一文，高张“文学革命军”大旗以示声援。他响亮地提出：“今欲革新政治，势不得不革新盘踞于运用此政治者精神界之文学。”这样，新文化运动的思想家们便把革新文学与革新政治有机地联系起来了。
\t\t\t\t文学革命的实质，是用民主主义的新文学，反对封建主义的旧文学。白话文取代文言文而通行于世，是文学革命同时也是新文化运动取得的显著成果之一。1 918年5月，鲁迅率先在《新青年》上发表白话小说《狂人日记》。这篇小说对吃人的封建礼教进行了血泪控诉和无情鞭挞，成为把文学革命的形式和内容相结合的典范，在中国近现代文学史上有着重要的地位和影响。钱玄同、刘半农、周作人等也积极参加了这场文学革命。
\t\t\t\t除了《新青年》以外，积极提倡新文化、传播新思想的报刊，还有《每周评论》、《国民》、《新潮》、《少年中国》、《建设》、《星期评论》、《改造》、《晨报》、《京报》等。
\t\t\t\t新文化运动对封建主义旧文化的全面出击，引起封建卫道士们的仇视和围攻。他们攻击新文化运动破坏孔教、礼法、国粹、贞节等，是“叛亲灭伦”、“禽兽自语”；哀叹“功利倡而廉耻丧，科学尊而礼义亡”。
`}</Text>
