                        <Text>{`\t\t\t\t这场战争改变了各帝国主义国家在中国的利益格局。日本利用欧洲列强忙于战场厮杀，暂时放松对中国侵略的时机，夺取了德国从清政府手中攫取的在山东的各种权益，并进一步加紧对中国的侵略和掠夺。1915年，日本政府向北洋军阀袁世凯政府提出灭亡中国的“二十一条”。袁世凯死后，日本又扶植段祺瑞政府，并通过“西原借款”①和其他一笔笔巨额贷款，获得了过去连袁世凯也不敢让与的许多特殊权益。1918年5月，段祺瑞政府和日本政府先后签订旨在反对苏维埃俄国的中日《陆军共同防敌军事协定》和《海军共同防敌军事协定》，规定日军可以在“共同防敌”的名义下，大批开入中国东北和蒙古地区，并可以指挥有关的中国军队。在此期间，段祺瑞政府聘请日本人担任政治、财政和军事顾问，并由日本派军官训练中国军队，使日本在华侵略势力迅速膨胀。这就打破了第一次世界大战前帝国主义列强共同控制中国的局面，加深了日本同其他列强之间的矛盾，从而也加剧了国内军阀之间的矛盾。
\t\t\t\t第一次世界大战使得西方资本主义制度固有的矛盾和弊端暴露无遗。战争的空前残酷，欧洲参战国在战后的种种衰败和混乱景象，人民群众生活的极端痛苦，人们精神的极度空虚和颓废，使西方资本主义文明完美优越的神话破灭。在战争期间，西方列强为了战争的需要，加紧掠夺中国的资源和战略物资，并征集大批人力到欧洲战场充当劳工。因此，中国人民也直接遭受到这场大战所造成的痛苦。
\t\t\t\t通过这场战争，中国的先进分子强烈地感受到西方资本主义制度已失去光明的前途，并对西方文明价值产生怀疑和动摇，由此开始对西方资本主义制度进行反思和批判。他们认为，第一次世界大战“使欧洲文明之权威大生疑念”，“欧人自己亦对于其文明之真价不得不加以反省”①。他们还进一步表示：“我们相信世界上的军国主义和金力主义，已经造了无穷罪恶，现在是应该抛弃的了。”`}</Text>
