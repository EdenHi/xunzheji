                        <Text>{`
\t\t\t\t经过五四爱国运动，中国人民有了新的觉醒。特别是青年中的一批先进分子，以救国救民、改造社会为己任，重新考虑中国的前途，探求改造中国社会的新方案。他们纷纷撰写文章、创办刊物或成立社团，以介绍、传播和研究国外的各种新思潮。当时，鼓吹新思潮的刊物如雨后春笋，先后出现400多种。这些刊物的绝大多数都宣称以改造社会为宗旨，有的还在文章中提出各种各样的改造中国社会的方案。
\t\t\t\t由于巴黎和会打破了人们对于帝国主义列强的幻想，更由于俄国十月革命的影响渐次扩大，因此，五四爱国运动后中国思想界出现一个明显特点，就是有相当一部分人在否定封建主义的同时，也开始怀疑以至放弃资产阶级共和国的方案，转而向往社会主义，认为“社会主义是现时和将来的人类共同的思想”。社会主义学说开始成为新思潮的主流。
\t\t\t\t新思潮来势汹涌，冲破传统思想的禁锢，使中国人民的思想得到一次大解放。当然，潮流涌来时难免泥沙俱下。五四时期被中国人当作新思潮传播的社会主义学说十分庞杂，既有马克思主义的科学社会主义，又有各种各样被称为“社会主义”的资产阶级和小资产阶级的思想流派，如无政府主义、无政府工团主义、互助主义、新村主义、合作主义、泛劳动主义、基尔特社会主义、伯恩施坦主义等。这时的情况是，“帝国主义压迫的切骨的痛苦，触醒了空泛的民主主义的噩梦”，“所以，学生运动倏然一变而倾向于社会主义”①。但绝大多数的进步青年对社会主义只是抱着一种朦胧的向往。他们对各种社会主义学说的了解，如同“隔着纱窗看晓雾”②，并不十分清晰。对这些主义和学说，他们正在进行分析、比较和选择，希望能从中找到挽救民族危亡和改造中国社会的良方。
`}</Text>
