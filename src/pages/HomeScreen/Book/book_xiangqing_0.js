                        <Text>{`1922年以后，中国民族工业开始出现危机，许多企业由于竞争不过外国资本的企业而停产歇业，有一些被外国资本吞并或控制。一些民族资本家在经营工业失败之后，转而从商业、金融业的投机中寻找出路，结果反过来又增加了工业的危机。这表明，中国民族资本主义经济在帝国主义的压迫和封建主义的束缚下，即使有偶然振兴的机会，最终还是不能得到充分发展。
\t\t\t\t就中国的经济情况来看，这时近代工业的发展水平还是很低的。1920年，全国工农业总产值为159．28亿元，其中近代工业产值为9.88亿元，占工农业总产值的6.2％，只有手工业总产值44.45亿元的22.2％。而且，中国资本主义工业的分布，在地区上极不平衡，绝大部分近代工业集中在沿海地区，特别是少数大城市。在民族工业中，主要是纺织、食品等轻工业比较发达，重工业极为薄弱，同时绝大多数企业规模狭小，资金短缺，技术设备陈旧，企业管理落后，不仅在市场上缺乏竞争力，而且对外国资本有很大的依赖性。
\t\t\t\t近代工业特别是民族资本主义经济一定程度的发展，引起中国社会阶级关系的新变动，促进了新的革命力量的成长。辛亥革命后，特别是第一次世界大战期间，在中国资产阶级有所发展的同时，中国无产阶级也有了进一步的发展。
\t\t\t\t无产阶级是以机器大工业为物质技术基础的资本主义生产方式的产物。中国无产阶级最早产生在19世纪中叶的外国在华企业中，随后又出现在19世纪60年代清朝官办的企业和70年代兴起的民族资本企业中。到1894年，中国近代产业工人约有10刀人a到1914年，增加到100万人以上。到1919年五四运动前夕，已达200万人以上。无产阶级日益成为近代中国一支重要的社会力量。`}</Text>
