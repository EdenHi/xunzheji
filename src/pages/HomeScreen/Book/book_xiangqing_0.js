                        <Text>{`他们从俄国十月革命的启示和西方资本主义的社会政治危机中，敏锐地感受到世界历史潮流的深刻变化，并且很快在实践中得出向俄国革命学习、“走俄国人的路”①的结论。随着俄国十月革命的胜利，欧美革命风暴的掀起以及亚洲各国民族解放运动的高涨，迫切需要有一个世界性的无产阶级政党组织，对各国革命斗争进行协调和指导。以列宁为首的布尔什维克党，承担起筹建这样一个新的国际组织的任务。1919年1月，在莫斯科举行了筹备建立共产国际(又称第三国际)的会议。3月2日，国际共产主义代表大会在莫斯科克里姆林宫举行。来自欧洲、美洲和亚洲21个国家的35个政党和组织的代表共52人出席会议。中国旅俄华工联合会的两位负责人刘绍周和张永奎，以“中国社会主义工人党”代表的名义，应邀出席了共产国际成立大会。列宁主持大会，并在会上作《关于资产阶级民主和无产阶级专政的提纲和报告》，强调无产阶级必须通过暴力革命来实现无产阶级专政。会议经过充分讨论，决定成立共产国际，并通过列宁所作的报告以及《共产国际行动纲领》、《共产国际章程草案》和《共产国际宣言》等重要文件。这些文件规定了共产国际的纲领和组织原则。
\t\t\t\t共产国际成立后，在指导欧美各国无产阶级政党进行革命斗争的同时，也开始密切关注亚洲各国特别是中国的革命运动情况，帮助这些国家的无产阶级建立自己的政党组织，并积极支持和推动这些国家的民族解放运动和人民革命斗争的发展。
\t\t\t\t总起来看，中国自鸦片战争失败以后，一步一步变为半殖民地半封建社会。为了挽救国家和民族的危亡，中国人民进行了艰苦卓绝的斗争；中国的先进分子历经千辛万苦，探索救国救民的真理，尝试过种种改造中国社会的方案。这些探索和斗争，虽然每一次都在一定历史条件下推动了中国历史的进步，但却都未能改变中国半殖民地半封建的社会性质和中国人民的悲惨命运。
`}</Text>
